#!/usr/bin/env python3
"""Prompt-side regeneration for wrong-function compile gates.

This runner is intentionally narrow.  It is used after
`wrong_function_regeneration_gate` has classified a rejected module-name repair:
the missing module must be regenerated by an LLM from public harness/interface
evidence, not synthesized by a deterministic hard guard.
"""
from __future__ import annotations

import argparse
import json
import re
import shutil
import subprocess
import sys
import time
from datetime import datetime, timezone
from pathlib import Path

from compile_skill_library import render_compile_skill_guidance
from generate import call_model, extract_code_blocks, infer_module_name, list_bench_task_dirs, read_meta
from score import score_one_task


ROOT = Path(__file__).resolve().parents[1]


def _json_read(path: Path) -> dict:
    return json.loads(path.read_text(encoding="utf-8"))


def _json_write(path: Path, data: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2, sort_keys=True) + "\n", encoding="utf-8")


def _model_slug(model: str) -> str:
    return model.replace("/", "_")


def _copy_sample(src: Path, dst: Path) -> None:
    if dst.exists():
        shutil.rmtree(dst)
    dst.mkdir(parents=True, exist_ok=True)
    for path in sorted(src.glob("*")):
        if path.is_file():
            shutil.copy2(path, dst / path.name)


def _read_result(result_root: Path, task_id: str) -> dict:
    for name in ("evas_result.json", "result.json"):
        path = result_root / task_id / name
        if path.exists():
            return _json_read(path)
    raise FileNotFoundError(f"missing result for {task_id} under {result_root}")


def _notes(result: dict) -> list[str]:
    return [str(note) for note in (result.get("evas_notes") or result.get("notes") or [])]


def _missing_module_from_notes(notes: list[str]) -> tuple[str, str] | None:
    text = " ".join(notes)
    match = re.search(r"undefined_module=([^;|\s]+);available_modules=([^|\s]+)", text)
    if not match:
        return None
    missing = [item for item in match.group(1).split(",") if item and item != "<none>"]
    available = [item for item in match.group(2).split(",") if item and item != "<none>"]
    if len(missing) != 1 or len(available) != 1:
        return None
    return missing[0], available[0]


def _tb_for_missing_module(task_dir: Path, missing_module: str) -> Path:
    candidates = sorted((task_dir / "gold").glob("tb*.scs"))
    for tb_path in candidates:
        text = tb_path.read_text(encoding="utf-8", errors="ignore")
        if re.search(rf"\b{re.escape(missing_module)}\b", text):
            return tb_path
    if candidates:
        return candidates[0]
    raise FileNotFoundError(f"no public harness found for {task_dir}")


def _include_filename_for_module(tb_text: str, missing_module: str) -> str:
    include_matches = re.findall(r'ahdl_include\s+"([^"]+\.va)"', tb_text)
    for filename in include_matches:
        if Path(filename).stem == missing_module:
            return filename
    return f"{missing_module}.va"


def _spectre_logical_statements(tb_text: str) -> list[str]:
    statements: list[str] = []
    current: list[str] = []
    for raw_line in tb_text.splitlines():
        line = raw_line.rstrip()
        stripped = line.strip()
        if not stripped:
            if current:
                statements.append(" ".join(current).strip())
                current = []
            continue
        if stripped.endswith("\\"):
            current.append(stripped[:-1].strip())
            continue
        current.append(stripped)
        statements.append(" ".join(current).strip())
        current = []
    if current:
        statements.append(" ".join(current).strip())
    return statements


def _instance_excerpt(tb_text: str, missing_module: str) -> str:
    include_lines: list[str] = []
    for line in tb_text.splitlines():
        stripped = line.strip()
        if re.search(rf'^\s*ahdl_include\s+"{re.escape(missing_module)}\.va"', stripped):
            include_lines.append(stripped)

    instance_lines: list[str] = []
    for stmt in _spectre_logical_statements(tb_text):
        stripped = stmt.strip()
        if not stripped or stripped.startswith("//") or stripped.startswith("ahdl_include"):
            continue
        if re.search(rf"\)\s*{re.escape(missing_module)}\b", stripped):
            instance_lines.append(stripped)
            break

    if include_lines or instance_lines:
        return "\n".join([*include_lines, *instance_lines]).strip()

    lines = tb_text.splitlines()
    excerpts: list[str] = []
    for idx, line in enumerate(lines):
        if re.search(rf"\b{re.escape(missing_module)}\b", line):
            start = max(0, idx - 3)
            end = min(len(lines), idx + 2)
            excerpts.extend(lines[start:end])
            break
    return "\n".join(excerpts).strip()


def _module_names_in_sample(sample_dir: Path) -> list[str]:
    names: list[str] = []
    for va_path in sorted(sample_dir.glob("*.va")):
        text = va_path.read_text(encoding="utf-8", errors="ignore")
        names.extend(re.findall(r"\bmodule\s+([A-Za-z_]\w*)", text))
    return names


def _current_candidate_excerpt(sample_dir: Path, limit: int = 240) -> str:
    chunks: list[str] = []
    for va_path in sorted(sample_dir.glob("*.va")):
        text = va_path.read_text(encoding="utf-8", errors="ignore")
        lines = text.splitlines()[:limit]
        chunks.append(f"// File: {va_path.name}\n" + "\n".join(lines))
    return "\n\n".join(chunks)


def _build_prompt(
    *,
    task_id: str,
    task_dir: Path,
    sample_dir: Path,
    missing_module: str,
    available_module: str,
    tb_path: Path,
    include_filename: str,
    result: dict,
) -> str:
    prompt_text = (task_dir / "prompt.md").read_text(encoding="utf-8", errors="ignore")
    tb_text = tb_path.read_text(encoding="utf-8", errors="ignore")
    skill_text = render_compile_skill_guidance(["wrong_function_regeneration_gate"])
    instance_excerpt = _instance_excerpt(tb_text, missing_module)
    module_names = ", ".join(_module_names_in_sample(sample_dir)) or "<none>"
    notes = "\n".join(f"- {note}" for note in _notes(result))

    return f"""\
# Wrong-Function Prompt-Side Regeneration

Task id: `{task_id}`

You are repairing one compile-gated generated artifact.  The previous local
module-name repair was rejected because it only changed the label of the wrong
function body.  Regenerate the missing public module.  Do not rewrite unrelated
artifacts.

## Compile skill guidance

{skill_text}

## Public task prompt

{prompt_text}

## Public harness evidence

The verifier harness includes `{include_filename}` and instantiates the missing
module `{missing_module}`.  Relevant public harness excerpt:

```spectre
{instance_excerpt}
```

Current strict-EVAS notes:

```text
{notes}
```

Current generated module names in the sample: `{module_names}`.
The available module `{available_module}` is not a safe replacement for
`{missing_module}`.

## Required output

Return exactly one fenced `verilog` code block containing a Verilog-A module
named `{missing_module}`.  The code will be saved as `{include_filename}`.

Requirements:
- Use only public interface/harness information above.
- Match the public harness instance arity and port order.
- Use voltage-domain `electrical` ports.
- Keep Spectre-compatible Verilog-A syntax.
- Do not include a testbench.
- Do not include hidden-gold assumptions, task-id templates, or explanations.
"""


def _choose_module_block(response_text: str, module_name: str) -> str | None:
    blocks = extract_code_blocks(response_text).get("va", [])
    for block in blocks:
        if infer_module_name(block) == module_name:
            return block
    return blocks[0] if blocks else None


def _response_from_replay_va(path: Path) -> str:
    text = path.read_text(encoding="utf-8", errors="ignore").strip()
    return f"```verilog\n{text}\n```"


def _score_with_maintained_validator(args: argparse.Namespace, *, output_root: Path, model_slug: str) -> dict:
    validation_output_root = args.output_root.resolve()
    cmd = [
        sys.executable,
        str(ROOT / "runners" / "validate_benchmark_v2_gold.py"),
        "--backend",
        args.validation_backend,
        "--bench-dir",
        str(args.bench_dir),
        "--family",
        args.validation_family,
        "--candidate-dir",
        str(output_root),
        "--model",
        model_slug,
        "--sample-idx",
        str(args.sample_idx),
        "--output-dir",
        str(validation_output_root),
        "--timeout-s",
        str(args.timeout_s),
        "--task",
        args.task,
    ]
    if args.validation_backend in {"spectre", "both"}:
        cmd.extend(["--env", str(args.env), "--profile", args.profile, "--spectre-mode", args.spectre_mode])
        if args.keep_remote_files:
            cmd.append("--keep-remote-files")

    proc = subprocess.run(cmd, cwd=ROOT, capture_output=True, text=True, timeout=args.validator_timeout_s)
    summary_path = validation_output_root / "summary.json"
    summary = _json_read(summary_path) if summary_path.exists() else {}
    result: dict = {
        "status": "VALIDATOR_ERROR" if proc.returncode else "VALIDATOR_EVALUATED",
        "scores": {},
        "notes": [],
        "validator": {
            "backend": args.validation_backend,
            "returncode": proc.returncode,
            "stdout_tail": "\n".join((proc.stdout or "").splitlines()[-20:]),
            "stderr_tail": "\n".join((proc.stderr or "").splitlines()[-20:]),
            "summary": summary,
        },
    }
    primary_name = "evas_result.json" if args.validation_backend in {"evas", "both"} else "spectre_result.json"
    primary_path = validation_output_root / args.task / primary_name
    if primary_path.exists():
        primary = _json_read(primary_path)
        result.update(
            {
                "status": primary.get("status", result["status"]),
                "scores": primary.get("scores", {}),
                "notes": primary.get("notes", []),
                "checker_result": primary.get("checker_result", {}),
                "primary_result": str(primary_path),
            }
        )
    if args.validation_backend == "both":
        spectre_path = validation_output_root / args.task / "spectre_result.json"
        if spectre_path.exists():
            spectre = _json_read(spectre_path)
            result["spectre_status"] = spectre.get("status")
            result["spectre_scores"] = spectre.get("scores", {})
            result["spectre_notes"] = spectre.get("notes", [])
            result["spectre_result"] = str(spectre_path)
    return result


def run_one(args: argparse.Namespace) -> dict:
    source_root = args.source_generated_dir.resolve()
    output_root = args.output_generated_dir.resolve()
    model_slug = _model_slug(args.model)

    task_dirs = dict(list_bench_task_dirs(args.bench_dir, selected={args.task}))
    task_dir = task_dirs[args.task]
    source_sample = source_root / model_slug / args.task / f"sample_{args.sample_idx}"
    output_sample = output_root / model_slug / args.task / f"sample_{args.sample_idx}"
    _copy_sample(source_sample, output_sample)

    result = _read_result(args.source_result_root, args.task)
    missing_pair = _missing_module_from_notes(_notes(result))
    if not missing_pair:
        raise RuntimeError("source result does not contain a unique undefined_module note")
    missing_module, available_module = missing_pair
    tb_path = _tb_for_missing_module(task_dir, missing_module)
    tb_text = tb_path.read_text(encoding="utf-8", errors="ignore")
    include_filename = _include_filename_for_module(tb_text, missing_module)

    prompt = _build_prompt(
        task_id=args.task,
        task_dir=task_dir,
        sample_dir=output_sample,
        missing_module=missing_module,
        available_module=available_module,
        tb_path=tb_path,
        include_filename=include_filename,
        result=result,
    )
    output_sample.mkdir(parents=True, exist_ok=True)
    (output_sample / "wrong_function_regeneration_prompt.md").write_text(prompt, encoding="utf-8")

    replay_source = ""
    call_mode = "api"
    started = time.perf_counter()
    if args.replay_va:
        replay_source = str(args.replay_va.resolve())
        call_mode = "replay_va"
        response_text = _response_from_replay_va(args.replay_va)
        usage = {"input_tokens": 0, "output_tokens": 0, "finish_reason": "replay_va"}
        api_error = ""
    elif args.replay_response:
        replay_source = str(args.replay_response.resolve())
        call_mode = "replay_response"
        response_text = args.replay_response.read_text(encoding="utf-8", errors="ignore")
        usage = {"input_tokens": 0, "output_tokens": 0, "finish_reason": "replay_response"}
        api_error = ""
    else:
        try:
            response_text, usage = call_model(args.model, prompt, args.temperature, args.top_p, args.max_tokens)
            api_error = ""
        except Exception as exc:  # noqa: BLE001 - provider boundary is reported in manifest.
            response_text = ""
            usage = {"input_tokens": 0, "output_tokens": 0, "finish_reason": "api_error"}
            api_error = f"{type(exc).__name__}: {str(exc)[:600]}"
    api_elapsed = time.perf_counter() - started
    (output_sample / "wrong_function_raw_response.txt").write_text(response_text, encoding="utf-8")
    block = _choose_module_block(response_text, missing_module)
    saved_files: list[str] = []
    if block:
        out_path = output_sample / include_filename
        out_path.write_text(block.strip() + "\n", encoding="utf-8")
        saved_files.append(str(out_path))

    meta = {
        "mode": "wrong-function-regeneration-v0",
        "model": args.model,
        "model_slug": model_slug,
        "task_id": args.task,
        "sample_idx": args.sample_idx,
        "missing_module": missing_module,
        "available_module": available_module,
        "include_filename": include_filename,
        "source_generated_dir": str(source_root),
        "source_result_root": str(args.source_result_root.resolve()),
        "status": "api_error" if api_error else ("generated" if saved_files else "no_code_extracted"),
        "error": api_error,
        "call_mode": call_mode,
        "replay_source": replay_source,
        "saved_files": saved_files,
        "api_elapsed_s": round(api_elapsed, 3),
        "api_call_count": 0 if call_mode.startswith("replay") else 1,
        "generated_at": datetime.now(timezone.utc).isoformat(),
        **usage,
    }
    _json_write(output_sample / "generation_meta.json", meta)

    if api_error:
        scored = {
            "status": "API_ERROR",
            "scores": {},
            "evas_notes": [api_error],
        }
    elif args.validation_backend == "legacy-score":
        scored = score_one_task(
            args.task,
            task_dir,
            output_sample,
            args.output_root,
            model=model_slug,
            sample_idx=args.sample_idx,
            temperature=args.temperature,
            top_p=args.top_p,
            timeout_s=args.timeout_s,
        )
    else:
        scored = _score_with_maintained_validator(args, output_root=output_root, model_slug=model_slug)
    manifest = {
        **meta,
        "output_generated_dir": str(output_root),
        "output_root": str(args.output_root.resolve()),
        "validation_backend": args.validation_backend,
        "score_status": scored.get("status"),
        "score_scores": scored.get("scores", {}),
        "score_notes": scored.get("evas_notes") or scored.get("notes") or [],
        "checker_result": scored.get("checker_result", {}),
    }
    if scored.get("validator"):
        manifest["validator"] = scored["validator"]
    if scored.get("primary_result"):
        manifest["primary_result"] = scored["primary_result"]
    if scored.get("spectre_status"):
        manifest["spectre_status"] = scored["spectre_status"]
        manifest["spectre_scores"] = scored.get("spectre_scores", {})
        manifest["spectre_notes"] = scored.get("spectre_notes", [])
        manifest["spectre_result"] = scored.get("spectre_result")
    _json_write(output_root / "wrong_function_regeneration_manifest.json", manifest)
    return manifest


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--bench-dir", required=True, type=Path)
    parser.add_argument("--source-generated-dir", required=True, type=Path)
    parser.add_argument("--source-result-root", required=True, type=Path)
    parser.add_argument("--output-generated-dir", required=True, type=Path)
    parser.add_argument("--output-root", required=True, type=Path)
    parser.add_argument("--task", default="completion92_calibration_bugfix")
    parser.add_argument("--model", default="kimi-k2.5")
    parser.add_argument("--sample-idx", type=int, default=0)
    parser.add_argument("--temperature", type=float, default=0.0)
    parser.add_argument("--top-p", type=float, default=1.0)
    parser.add_argument("--max-tokens", type=int, default=4096)
    parser.add_argument("--timeout-s", type=int, default=240)
    parser.add_argument(
        "--validation-backend",
        choices=["legacy-score", "evas", "spectre", "both"],
        default="evas",
        help="Use the maintained validator by default so benchmark-balanced checker.py routing is honored.",
    )
    parser.add_argument("--validation-family", default="benchmark-balanced")
    parser.add_argument("--validator-timeout-s", type=int, default=900)
    parser.add_argument("--env", default=str(ROOT / "virtuoso-bridge-lite" / ".env"))
    parser.add_argument("--profile", default="ci")
    parser.add_argument("--spectre-mode", default="spectre", choices=["spectre", "aps", "x", "cx", "ax", "mx", "lx", "vx"])
    parser.add_argument("--keep-remote-files", action="store_true")
    parser.add_argument(
        "--replay-response",
        type=Path,
        help="Offline validation mode: parse a saved model response instead of calling the provider.",
    )
    parser.add_argument(
        "--replay-va",
        type=Path,
        help="Offline validation mode: wrap an existing generated .va file as the model response.",
    )
    args = parser.parse_args()
    if args.replay_response and args.replay_va:
        parser.error("--replay-response and --replay-va are mutually exclusive")
    return args


def main() -> int:
    manifest = run_one(parse_args())
    print(json.dumps({
        "task_id": manifest["task_id"],
        "status": manifest["score_status"],
        "scores": manifest["score_scores"],
        "output_generated_dir": manifest["output_generated_dir"],
    }, indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
