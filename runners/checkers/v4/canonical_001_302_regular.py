def install_regular_checkers_001_302(
    checks: dict[str, Checker],
    streaming_checks: dict[str, Checker],
) -> list[str]:
    missing = sorted(set(CHECKERS).difference(checks))
    if missing:
        raise KeyError(f"canonical checker ids missing before migration: {missing}")
    missing_streaming = sorted(set(STREAMING_CHECKERS).difference(streaming_checks))
    if missing_streaming:
        raise KeyError(f"canonical streaming checker ids missing before migration: {missing_streaming}")
    LEGACY_CHECKERS.clear()
    LEGACY_CHECKERS.update({checker_id: checks[checker_id] for checker_id in CHECKERS})
    LEGACY_STREAMING_CHECKERS.clear()
    LEGACY_STREAMING_CHECKERS.update(
        {checker_id: streaming_checks[checker_id] for checker_id in STREAMING_CHECKERS}
    )
    checks.update(CHECKERS)
    streaming_checks.update(STREAMING_CHECKERS)
    return sorted(CHECKERS)
